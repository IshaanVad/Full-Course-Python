# type casting = convert the data type of a value to another data type

x = 1 # int
y = 2.0 # float
z = "3" # str (string)

# can cast to various types
x = int(x)
y = int(y)
z = int(z)

x = float(x)
y = float(y)
z = float(z)

x = str(x)
y = str(y)
z = str(z)

# can be in any type wanted, except if a string is within the print
# solution: print("Blah blah blah" + str(variable))
print(x) 
print(y)
print(z * 3)